// Utility: get unique categories from the catalog
export function getCatalogCategories(): string[] {
  const cats = new Set(FOOD_CATALOG.map(item => item.category));
  return Array.from(cats);
}

// Utility: compute cost per serving from catalog item
export function costPerServing(item: CatalogItem): number {
  if (item.pack_servings === 0) return 0;
  return Math.round((item.estimated_cost / item.pack_servings) * 100) / 100;
}
